const grid = new Array(1000).fill(0).map(() => new Array(1000).fill(0));
const visited = new Array(1000).fill(0).map(() => new Array(1000).fill(0));

const [x, y] = [500, 500];
let [hx, hy] = [x, y];
let [tx, ty] = [x, y];

grid[hy][hx] = 1;
visited[ty][tx] = 1;

const move = (x, y, dir) => {
    switch (dir) {
        case "U":
            return [x, y - 1];
        case "D":
            return [x, y + 1];
        case "L":
            return [x - 1, y];
        case "R":
            return [x + 1, y];
    }
}

const isAdjacent = (x1, y1, x2, y2) => {
    return Math.abs(x1 - x2) <= 1 && Math.abs(y1 - y2) <= 1;
}

const isDiagonal = (x1, y1, x2, y2) => {
    return Math.abs(x1 - x2) === 1 && Math.abs(y1 - y2) === 1;
}

const isSameRow = (x1, x2) => {
    return x1 === x2;
}

const isSameColumn = (y1, y2) => {
    return y1 === y2;
}

const isSamePosition = (x1, y1, x2, y2) => {
    return x1 === x2 && y1 === y2;
}

const isTouching = (x1, y1, x2, y2) => {
    return isAdjacent(x1, y1, x2, y2) || isDiagonal(x1, y1, x2, y2);
}

const updateTail = (hx, hy, tx, ty) => {
    if (isSameRow(hx, tx)) {
        if (hx < tx) {
            return [tx - 1, ty];
        } else {
            return [tx + 1, ty];
        }
    } else if (isSameColumn(hy, ty)) {
        if (hy < ty) {
            return [tx, ty - 1];
        } else {
            return [tx, ty + 1];
        }
    } else {
        if (hx < tx && hy < ty) {
            return [tx - 1, ty - 1];
        } else if (hx < tx && hy > ty) {
            return [tx - 1, ty + 1];
        } else if (hx > tx && hy < ty) {
            return [tx + 1, ty - 1];
        } else {
            return [tx + 1, ty + 1];
        }
    }
}

const updateHead = (hx, hy, tx, ty, dir, steps) => {
    for (let i = 0; i < steps; i++) {
        [hx, hy] = move(hx, hy, dir);
        if (!isTouching(hx, hy, tx, ty) && !isSamePosition(hx, hy, tx, ty)) {
            [tx, ty] = updateTail(hx, hy, tx, ty);
        }
        visited[ty][tx] = 1;
    }
    return [hx, hy, tx, ty];
}


